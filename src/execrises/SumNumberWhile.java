public class SumNumberWhile {

	public static void main(String[] args) {
		
				int i = 1;
				int num = 10;
				int sumNumber = 0;
				
				while (i<=num )
				{
					sumNumber = sumNumber + i;
					i++;
				}
				
				System.out.println("Sum of numbers: " + sumNumber);
									
			}

		}


