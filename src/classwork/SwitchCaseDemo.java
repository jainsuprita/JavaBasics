package classwork;

public class SwitchCaseDemo {

	public static void main(String[] args) {
		
		byte b =0;
		
		switch (b) {
		case 1 : 
			
			System.out.println("one");
			break;
		case 2 : 
			System.out.println("two");
			break;
		case 3 : 
			 System.out.println("three");
				break;
				
		default : 
					System.out.println("not a number");
		}

	}

}
